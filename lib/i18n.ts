export function isLocale(value: string): value is Locale {
  return value === 'sk' || value === 'en';
}

export function getMessages(locale: Locale) {
  return messages[locale];
}

export function toDateLocale(locale: Locale): string {
  return locale === 'sk' ? 'sk-SK' : 'en-US';
}

export function getSourceLabel(locale: Locale, source: string): string {
  const common = getMessages(locale).common;
  if (source === 'internal') return common.internal;
  if (source === 'facebook') return common.facebook;
  if (source === 'google') return common.google;
  if (source === 'instagram') return common.instagram;
  return source;
}

export function getStatusLabel(locale: Locale, status: string): string {
  const common = getMessages(locale).common;
  return status === 'published' ? common.published : common.draft;
}
